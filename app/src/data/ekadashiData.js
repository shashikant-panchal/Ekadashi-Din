// Function to get moon phase info (icon name)
export const getMoonPhaseIcon = (phase) => {
  switch (phase) {
    case 'new':
      return 'moon-outline';
    case 'waxing-crescent':
      return 'moon';
    case 'full':
      return 'sunny';
    case 'waning-crescent':
      return 'moon-outline';
    default:
      return 'moon-outline';
  }
};

// Function to get next Ekadashi
export const getNextEkadashi = () => {
  const today = new Date();
  return ekadashiData2025.find(ekadashi => ekadashi.date > today) || null;
};

// Function to get Ekadashi by month
export const getEkadashiByMonth = (month) => {
  return ekadashiData2025.filter(ekadashi => ekadashi.date.getMonth() === month);
};
